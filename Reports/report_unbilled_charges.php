function report_sales_summary($dbc, $starttime, $endtime, $table_style, $table_row_style, $grand_total_style) {

    $result5 = mysqli_query($dbc, "SELECT patientid,invoiceid,final_price, invoice_date, injuryid FROM invoice WHERE invoice_date >= '".$starttime."' AND invoice_date <= '".$endtime."' AND paid='Saved'");

    $report_data .= '<table border="1px" class="table table-bordered" style="'.$table_style.'">';
    $report_data .= '<tr style="'.$table_row_style.'">
    <th width="10%">Invoice #</th>
    <th width="10%">Invoice Date</th>
    <th width="20%">Customer</th>
    <th width="30%">Injury</th>
    <th width="10%">Invoice Total</th>
    </tr>';
    while($row5 = mysqli_fetch_array($result5)) {
        $report_data .= '<tr nobr="true">';
        $report_data .= '<td><a href=\'../Invoice/add_invoice.php?invoiceid='.$row5['invoiceid'].'&patientid='.$row5['patientid'].'&from=report&report_from='.$starttime.'&report_to='.$endtime.'\' >'.$row5['invoiceid'].'</a></td>';

        $report_data .= '<td>'.$row5['invoice_date'].'</td>';
        $report_data .= '<td><a href="../Contacts/add_contacts.php?category=Patient&contactid='.$row5['patientid'].'&from_url='.urlencode(WEBSITE_URL.$_SERVER['REQUEST_URI']).'">'.get_contact($dbc, $row5['patientid']). '</a></td>';
        $report_data .= '<td>' . get_all_from_injury($dbc, $row5['injuryid'], 'injury_name').' : '.get_all_from_injury($dbc, $row5['injuryid'], 'injury_type'). '</td>';
        $report_data .= '<td>$'.$row5['final_price'].'</td>';

        $report_data .= '</tr>';
    }

    $report_data .= '</table>';

    return $report_data;
}

